# multiple variables formatting
first_name = "John"
last_name = "Smith"
print("Hello, %s %s !" % (first_name, last_name))

def print_full_name(first_name, last_name):
    print("Hello, %s %s !" % (first_name, last_name))

print_full_name("Chavdar", "Momchilov")
print_full_name("Miroslav", "Ivanov")
print_full_name("Daniel", "Simeonov")
